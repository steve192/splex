#!/usr/bin/env node
/* eslint-disable */
const fs = require("fs");
const path = require("path");

const js = fs.readFileSync(require.resolve("leaflet/dist/leaflet.js"), "utf8");
const css = fs.readFileSync(require.resolve("leaflet/dist/leaflet.css"), "utf8");

const out = `// AUTO-GENERATED by scripts/build-leaflet-assets.js - do not edit by hand.
// Regenerated on every npm install (postinstall) and Metro startup (metro.config.js).

export const LEAFLET_JS = ${JSON.stringify(js)};

export const LEAFLET_CSS = ${JSON.stringify(css)};
`;

const target = path.join(__dirname, "..", "src", "shared", "ui", "leafletAssets.generated.ts");
fs.mkdirSync(path.dirname(target), { recursive: true });

// Only write when content changes so we don't retrigger Metro's file watcher on every start.
let existing = null;
try {
  existing = fs.readFileSync(target, "utf8");
} catch {
  /* file may not exist yet */
}
if (existing !== out) {
  fs.writeFileSync(target, out);
  if (require.main === module) {
    console.log(`Wrote ${target} (${(out.length / 1024).toFixed(1)} KiB)`);
  }
}

module.exports = { regenerate: () => undefined };
